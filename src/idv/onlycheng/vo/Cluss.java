package idv.onlycheng.vo;

public class Cluss {
	private String name;
	private int id;
	private String address;

	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *                 set name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id
	 *               set id
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return address
	 */
	public String getAddress() {
		return address;
	}

	/**
	 * @param address
	 *                    set address
	 */
	public void setAddress(String address) {
		this.address = address;
	}

}
